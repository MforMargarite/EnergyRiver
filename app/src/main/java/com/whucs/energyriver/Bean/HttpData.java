package com.whucs.energyriver.Bean;

import android.util.Log;

//返回数据的请求结果
public class HttpData<T> {
    private int code;
    private T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
